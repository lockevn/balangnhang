II.1.11    Quản trị thẻ học và nạp thẻ

a.       Giá trị lớn nhất của thẻ học nằm ở mã PIN. Mã PIN dùng để kích hoạt tài khoản và thể hiện giá trị sử dụng của tài khoản tương ứng với số tiền ghi trên thẻ.

b.       Xuất các loại thẻ học theo mệnh giá quy định.

c.       Theo dõi thẻ học qua số series.

d.       Khuyến mãi bằng cách cộng thêm số ngày sử dụng tùy ý vào các thẻ học. Cộng vào số series thẻ sẽ khuyến mãi.

e.       Ngăn chặn truy cập vào các nội dung học tập trên các khóa học của các tài khoản nạp thẻ đã hết hạn, nhưng không ngăn chặn truy cập vào các tính năng khác của SW-elearning.

f.         Các hình thức tính học phí trên thẻ học: Nhằm tạo ra cơ chế mở mà vẫn đảm bảo quyền lợi tài chính của Công ty, SW-V3 có chế độ tính phí thông minh hơn như sau:

--> Thach (quản lý thẻ học theo hình thức thời gian như ban đầu) Khi hết hạn thẻ thì học viên không thể truy cập các resource là webpage, các activity là Quiz, Assignment

Giới hạn thời gian sử dụng thẻ: vd thẻ 150K thòi hạn 60 ngày (còn tiền nhưng 0 đc học)

-          Một ngày được tính từ 0h.00 tới 24h.00.

-          Tài khoản học viên luôn được trỏ vào khoá học chính mà học viên đã lựa chọn sau khi làm bài kiểm tra trình độ xong hoặc là khoá học mà học viên chủ động lựa chọn ở trang chủ. Ngày nào học viên đăng nhập vào bài học để học, tài khoản sẽ tự động trừ 5000 (mức phí đối với các khoá tiếng Anh tổng quát, ngữ pháp chuyên sâu và tiếng Anh dành cho học sinh phổ thông là 5000đ/ngày; mức phí với khoá tiếng Anh thương mại, luyện thi TOEIC và các khoá tiếng Anh chuyên ngành là 8000đ/ngày). Tuy nhiên, ngay khi học viên chủ động đăng nhập vào khoá học khác, không phải là khoá học mặc định của học viên đó thì tài khoản sẽ bị trừ thêm tiền tương ứng với học phí theo ngày của khoá học mà học viên truy cập thêm. Ví dụ một học viên có tài khoản ở khoá tiếng Anh tổng quát trình độ pre-intermediate 1, nhưng lại truy cập vào khoá Pre2, thì học viên đó phải trả thêm 5000đ cho lần truy cập thêm đó nữa. Hoặc học viên có tài khoản Pre1 mà truy cập thêm khoá TOEIC 1, học viên phải trả thêm 8000đ lần truy cập đó nữa.

